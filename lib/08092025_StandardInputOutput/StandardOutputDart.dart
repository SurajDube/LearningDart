/* In Dart, there are two ways to display output in the console: 

Using print statement.
Using stdout.write() statement.*/

import 'dart:io';

void main()
{
    // Printing in first way
    print("Welcome to Suraj Dubey. // printing from print() statement");

    // Printing in second way
    stdout.write("Welcome to Suraj Dubey. // printing from stdout.write()");
}

//Explanation:
//Note: The print() statement brings the cursor to the next line while stdout.write() don't bring the cursor to the next line, it remains in the same line.
