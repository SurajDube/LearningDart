// Dart program to demonstrate
// String Data Type

void main() {
      
      // Declare and initialize a string
      String string = "Suraj Kumar Dubey";
    
      // Declare two separate string variables
      String str = 'Coding is ';
      String str1 = 'Fun';
    
      // Print the string variable
      print(string);
    
      // Concatenate and print the two strings
      print(str + str1); 
      
      // Output: Coding is Fun
      String name1 = 'suraj';
      String name2 = "suraj";
      String name3 = '"suraj"';
      String name4 = "'suraj'";
      print(name1);
      print(name2);
      print(name3);
      print(name4);
}